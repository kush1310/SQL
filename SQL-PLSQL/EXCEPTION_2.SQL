DECLARE
	A INT;
	B INT;
	ANS INT;
BEGIN
	A := &A;
	B := &B;
	ANS := A/B;
	DBMS_OUTPUT.PUT_LINE('DIVISION OF A AND B IS: ' || ANS);

EXCEPTION
	WHEN ZERO_DIVIDE THEN
		DBMS_OUTPUT.PUT_LINE('ERROR! CANNOT DIVIDE A NUMBER BY ZERO');
		DBMS_OUTPUT.PUT_LINE('RE-CHECK THE VALUES..');
		DBMS_OUTPUT.PUT_LINE('A = ' || A);
		DBMS_OUTPUT.PUT_LINE('B = ' || B);
END;
